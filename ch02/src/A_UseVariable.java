
public class A_UseVariable {

	public static void main(String[] args) {

		int num1;
		num1 = 10;

		int num2 = 20;
		int add;
		add = num1 + num2;

		System.out.println(add);
		System.out.println(num1 + "+" + num2 + "=" + add);

		double db1;
		db1 = 10.1;

		double db2 = 20.23;
		double db3;
		db3 = db1 + db2 + num1;

		System.out.println(db3);
		System.out.println(db1 + "+" + db2 + "=" + db3);

	}

}
